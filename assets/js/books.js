const LIVROS_LONER_AUTHOR_FOLDERS = {
  Brasileiro: 'brasil',
  Brasileira: 'brasil',
  Moçambicana: 'mocambique',
  Português: 'portugal',
  Tcheco: 'tchequia',
  Francês: 'franca'
};

window.LIVROS_LONER_BOOKS.forEach(book => {
  const folder = LIVROS_LONER_AUTHOR_FOLDERS[book.authorNationality];
  book.page = `livros/${folder}/${book.slug}.html`;
});

(() => {
  const books = window.LIVROS_LONER_BOOKS;
  const root = document.body?.dataset.root || '';
  const bookHref = slug => {
    const book = books.find(item => item.slug === slug);
    return `${root}${book?.page || 'livros/index.html'}`;
  };
  const coverSrc = cover => `${root}assets/images/livros/${cover}`;

  const catalog = document.querySelector('[data-book-catalog-list]');
  if (catalog) {
    catalog.innerHTML = books.map(book => `
      <article class="catalog-item book-item" data-catalog-item data-title="${book.title}" data-letter="${book.title.charAt(0).toUpperCase()}" data-year="${book.originalYear}" data-genre="${book.genreKey}" data-publisher="${book.publisher.toLowerCase().replace(/[^a-z0-9]+/g, '-')}" data-language="portugues" data-series="edicao-brasileira" data-format="capa-comum" data-search="${book.title} ${book.originalTitle} ${book.author} ${book.publisher} ${book.genre}">
        <a href="${bookHref(book.slug)}"><img src="${coverSrc(book.cover)}" alt="Capa de ${book.title}"></a>
        <div class="item-copy"><span class="item-label">${book.label}</span><h3><a href="${bookHref(book.slug)}">${book.title}</a></h3><div class="item-meta"><span>${book.author}</span><span>${book.originalYear}</span><span>${book.country}</span><span>${book.publisher}</span><span>${book.pages} páginas</span></div><p>${book.summary}</p></div>
        <a class="item-action" href="${bookHref(book.slug)}" aria-label="Abrir ${book.title}">→</a>
      </article>`).join('');
  }

  const detail = document.querySelector('[data-book-detail]');
  if (detail) {
    const slug = document.body.dataset.bookSlug || new URLSearchParams(location.search).get('slug');
    const book = books.find(item => item.slug === slug) || books[0];
    document.title = `${book.title} — Livros Loner`;
    const setText = (selector, value) => {
      document.querySelectorAll(selector).forEach(element => { element.textContent = value; });
    };
    setText('[data-detail-title]', book.title);
    setText('[data-detail-original]', book.originalTitle);
    setText('[data-detail-label]', book.label);
    setText('[data-detail-summary]', book.summary);
    setText('[data-detail-author]', book.author);
    setText('[data-detail-author-nationality]', book.authorNationality);
    setText('[data-detail-year]', book.originalYear);
    setText('[data-detail-country]', book.country);
    setText('[data-detail-genre]', book.genre);
    setText('[data-detail-about]', book.about);
    setText('[data-detail-publisher]', book.publisher);
    setText('[data-detail-pages]', book.pages);
    setText('[data-detail-isbn]', `Copiar ISBN ${book.isbn}`);
    setText('[data-breadcrumb-title]', book.title);
    const cover = document.querySelector('[data-detail-cover]');
    cover.src = coverSrc(book.cover); cover.alt = `Capa de ${book.title}`;
    const source = document.querySelector('[data-detail-source]'); source.href = book.source;
    const isbn = document.querySelector('[data-detail-isbn]'); isbn.dataset.copyIsbn = book.isbn;
    const tracker = document.querySelector('[data-reading-tracker]');
    tracker.dataset.bookId = book.slug; tracker.dataset.bookTitle = book.title; tracker.dataset.bookSlug = book.slug;
    tracker.dataset.bookCover = `assets/images/livros/${book.cover}`; tracker.dataset.totalPages = book.pages;
    const pages = document.querySelector('[data-pages-read]'); pages.max = book.pages;
    setText('[data-reading-label]', `0 de ${book.pages} páginas · 0 XP`);
  }
})();
